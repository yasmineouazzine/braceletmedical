    String dno = lbldno.getText();
    String dname = txtdname.getText();
    String spl = txtspl.getText();
    String phone = txtphone.getText();
    String tem = txttem.getText();
    String bpm = txtbpm.getText();

    try {
        pst = con.prepareStatement("insert into braceletinfo(Bracelet_id,Full_Name,Phone,Relative_Phone,Temp_measure,Bloodpress_measure)values(?,?,?,?,?,?)");

        // Correct parameter indexing from 0-based to 1-based
        pst.setString(1, dno);
        pst.setString(2, dname);
        pst.setString(3, spl);
        pst.setString(4, phone);
        pst.setString(5, tem);
        pst.setString(6, bpm);
        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "BraceletInfo Inserted");

        AutoID();
        txtdname.setText("");
        txtspl.setText("");
        txtphone.setText("");
        txttem.setText("");
        txtbpm.setText("");
        txtdname.requestFocus();

        BraceletInfo_table();

    } catch (SQLException ex) {
        Logger.getLogger(Braceletinfo.class.getName()).log(Level.SEVERE, null, ex);
    }


