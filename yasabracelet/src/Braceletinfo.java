    public void AutoID() {
        try {
            Statement s = con.createStatement();
            rs = s.executeQuery("select MAX(Bracelet_id) from braceletinfo");
            rs.next();
            rs.getString("MAX(Bracelet_id)");

            if (rs.getString("MAX(Bracelet_id)") == null) {
                lbldno.setText("BI001");
            } else {
long id = Long.parseLong(rs.getString("MAX(Bracelet_id)").substring(2));
                id++;
                lbldno.setText("BI" + String.format("%03d", id));

            }

        } catch (SQLException ex) {
            Logger.getLogger(Braceletinfo.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void BraceletInfo_table() {

        try {
            pst = con.prepareStatement("select * from BraceletInfo ");
           
            rs = pst.executeQuery();
            ResultSetMetaData Rsm = rs.getMetaData();
            int c;
            c = Rsm.getColumnCount();
            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();

                for (int i = 1; i <= c; i++) {

                    v2.add(rs.getString("Bracelet_id"));
                    v2.add(rs.getString("Full_Name"));
                    v2.add(rs.getString("phone"));
                    v2.add(rs.getString("Relative_Phone"));
                    v2.add(rs.getString("Temp_measure"));
                    v2.add(rs.getString("Bloodpress_measure"));
                    

                  
                }
                df.addRow(v2);
            }

        } catch (SQLException ex) {
            Logger.getLogger(Braceletinfo.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

